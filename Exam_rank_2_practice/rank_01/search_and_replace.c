#include <unistd.h>

int	main(int ac, char *av[])
{
	if(ac == 4)
	{
		int	x;

		x = 0;
		while(av[1][x])
		{
			if(av[1][x] == av[2][0])
				av[1][x] = av[3][0];
		write(1, &av[1][x], 1);
		x++;
		}
	}
	write(1, "\n", 1);
}