#include <unistd.h>

int	main(int ac, char *av[])
{
	int	x;
	
	if(ac == 2)
	{
		x = 0;
		
		while(av[1][x])
		{
			if(av[1][0] >= 65 && av[1][0] <= 90)
				av[1][0] += 32;
			if(av[1][x] >= 65 && av[1][x] <= 90)
			{
				av[1][x] += 32;
				write(1, "_", 1);
			}
			write(1, &av[1][x], 1);
			x++;
		}
	}
	write(1, "\n", 1);
}