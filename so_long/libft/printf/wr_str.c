#include "ft_printf.h"

int	wr_str(char *s)
{
	int	x;

	x = 0;
	if (!s)
		return (x += write(1, "(null)", 6));
	while (s[x])
	{
		write(1, &s[x], 1);
		x++;
	}
	return (x);
}
